<div id="about">
    <h2 id="about-header">Why pictures?</h2>
    <p id="about-desc">Pictures are useful when learning languages because they help the "stream of consciousness" learning. Rather than translating the word from the target language to your main language, you implicitly understand the meaning of the word. For example, rather than looking at a tree, knowing the word in your main language, and then translating it to the target language, you simply look at the tree and know the word immediately. This is a major step towards fluency in any language.</p>
